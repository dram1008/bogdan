                Например выполняя свою официальную роль в одном мире, ангел может связать его с другим миром, которому
                он сам заинтересован помочь, и через эту связь передать ему часть энергии того пространства, в котором
                находится.</p>

            <p>Находясь в шестимерном пространстве, каждый ангел обладает доступом к протовеществу &ndash; универсальной
                энергии, на основе которой можно создать любые изменения в материальном мире. И тот мир, который
                обладает значительными запасами этой энергии, обладает силой для дальнейшего существования и развития.
                Поэтому потоки протовещества, которые создают ангелы между мирами, можно сравнить с денежными потоками
                существующими между странами на Земле. И в том случае, если ангелы действуют в интересах тех миров
                которые они связывают, то они похожи на политических деятелей, занимающихся экономическими
                взаимоотношениями между государствами. В то же время, каждый ангел являясь посредником, может
                действовать в личных интересах и оставлять часть энергии себе. Тогда его можно сравнить с
                предпринимателем, делающим бизнес на купле-продаже протовещества, или с банкиром зарабатывающим прибыль
                на курсе валют.</p>

            <p>В шестимерном пространстве организуются целые сообщества ангелов, похожие на бизнес-корпорации на Земле,
                преумножающие запасы собственной энергии. И если в человеческом социуме, такие организации существуют
                пока не более нескольких столетий, то деловые отношения ангелов развиваются уже многие миллиарды лет!
                Поэтому сила скопленная отдельными сообществами ангелов, может быть огромна. Ее может быть достаточно
                для того, чтобы создать отдельный материальный мир, существующий сам по себе вне ведома Иерархии, и
                такие попытки уже предприняты некоторыми сообществами. Или же наоборот, накопленную энергию можно
                направить на растворение любого процесса, проводящегося верхними уровнями, вплоть до уничтожения целых
                галактик и вселенных.</p>

            <p>Тем не менее сообщества ангелов, редко решаются на шаги которые будут заметны Иерархии, ведь тем самым
                они рискуют обнаружить себя. В том случае, если Иерархия замечает действия ангелов в собственных
                интересах, то она накладывает на каждого из них дополнительные ограничения, блокируя деятельность целого
                сообщества. Поэтому ангелы играющие по своим правилам, предпочитают не рисковать и действуют незаметно,
                подобно мафиозным группировкам. Они стараются не концентрировать свои действия в каком-то одном мире, а
                распределяют их на множество реальностей и создают целую сеть взаимоотношений. Старейшие и самые крупные
                организации ангелов, распространяются на все Мироздание, имея множество представителей в каждом
                материальном мире. Между ними безусловно существует и сотрудничество, и конкуренция, как и между любыми
                компаниями на Земле заинтересованными в прибыли.</p>

            <p>Каким же образом деятельность тайных сообществ ангелов проявляется в материальной реальности?</p>

            <p>Она может проявляться буквально во всем, что происходит на уровне материи в каждом из миров. Ведь ангелы
                некогда участвовали в ее формировании, а теперь поддерживают круговорот вещества на глобальном уровне.
                Обладая универсальным энергетическим ресурсом, они находят себе поддержку среди тех сущностей, которые
                проводят частные процессы в отдельных материальных мирах. К примеру на Земле, сообщества ангелов имеют
                своих представителей и среди духов природы формирующих все природные явления, и среди лирианцев
                участвующих в организации социальных процессов. И за многими явлениями которые вы наблюдаете, стоит то
                или иное объединение ангелов, являющихся источником энергии для его протекания. При этом сообществам
                ангелов, обычно удобнее действовать в социальной среде, нежели в природной. Это связано с тем, что
                природные процессы устроены более просто и прозрачно, и Иерархии намного легче их контролировать.
                Поэтому большая часть внимания таких ангелов, направлена на социальную систему и их роль в ней выражена
                наиболее явно.</p>

            <p>К примеру многие крупнейшие корпорации на Земле и даже целые государства, поддерживаются такими
                содружествами ангелов. При этом лидеры этих организаций, конечно не догадываются о том, какие силы стоят
                за ними на тонком плане. Тем не менее успех и процветание таких глобальных организаций в социуме, во
                многом зависит от сообществ ангелов дающих силу тем лирианцам, которые поддерживают эгрегор данной
                организации. Во многом благодаря энергии ангелов, эти организации смогли обойти своих конкурентов и
                занять лидирующие позиции в масштабе всей вашей планеты. Практически все, что происходит в социуме
                поделено между такими сообществами, которые отстаивают свои позиции или ведут тонкую игру по перехвату
                возможностей друг у друга. Конкуренция же, возникающая между всеми крупнейшими организациями и даже
                странами, часто связана со взаимоотношениями между группами ангелов. Более того, многие войны
                происходившие в разные времена на Земле, связаны с таким тонким противостоянием.</p>

            <p>Наверное у многих возникнет вопрос: &laquo;Неужели Иерархия не замечает этих проявлений и не может
                блокировать их?&raquo;. Конечно, на верхних уровнях заметны глобальные изменения создаваемые группами
                ангелов. Но для представителей Иерархии управляющих человеческим&nbsp; социумом, сообщества ангелов
                рассматриваются как ценный энергетический ресурс, который возможно направлять в нужное русло. Проявив
                себя в масштабных социальных процессах, сообщества ангелов становятся заметными, а значит и
                управляемыми. При этом Иерархия не торопится ограничивать их до тех пор, пока&nbsp; действия их не будут
                противоречить тому плану развития, который создан ею.</p>

            <p>В свою очередь сообщества ангелов, также понимают тот риск которому подвергают себя и стараются
                действовать в рамках плана Иерархии. Таким образом, между Иерархией и наиболее крупными сообществами
                ангелов создалось негласное сотрудничество, которое пока устраивает обе стороны. У ангелов есть запас
                накопленной энергии, а у Иерархии &ndash; возможность создавать для таких сообществ особые социальные
                условия, в которых они могут действовать более свободно. Например многие монополии существующие на
                экономическом рынке, являются проявлением таких особых условий, созданных Иерархией.</p>

            <p>В результате получается, что большая часть ангелов всего Мироздания, вне зависимости от того, есть ли у
                них желание действовать по плану Иерархии, так или иначе находится под ее контролем. И только некоторые
                сообщества ангелов, действующие наиболее тонко и незаметно, до сих пор остаются относительно свободными.
                Таких ангелов ведущих скрытную игру, можно назвать темными. При этом такое название совсем необязательно
                свидетельствует о том, что они действуют в личных или корыстных интересах. Оно означает лишь то, что
                действия таких ангелов не согласуются с планами Иерархии.</p>

            <p>Для того, чтобы оставаться незамеченными, темные ангелы не проявляются в масштабных процессах, они
                устанавливают контакт с отдельными существами в разных мирах и поддерживают их. Их интересы как и у
                любого сообщества ангелов, заключаются в возможности установить контроль над какой-то ситуацией в
                материальной реальности. Например, они открывают источник эмоциональной энергии для некоторых людей, и
                тем самым активируют их ценные качества и способности, позволяющие действовать в социуме особенно
                эффективно. Благодаря этому такие люди обретают значимое социальное положение, занимая высокие посты в
                крупных организациях или же создавая собственное дело. Получается, что хотя сообщества темных ангелов и
                не проявляются в масштабных процессах, они находят свои возможности влиять на ситуацию в социуме,
                действуя через ключевых людей, находящихся при управлении.</p>

