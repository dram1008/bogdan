                ангелы часто выполняют роль ангелов-хранителей, поддерживая людей в реализации предназначения. И хотя
                ангелам-хранителям приходится действовать по инструкциям переданным им сверху, обычно они поддерживают
                людей из чистых побуждений. Светлые намерения таких ангелов исходят из долгой связи, которую они
                устанавливают с отдельными людьми, и поддерживают ее множество их физических жизней.</p>

            <p>Кроме того, те ангелы которые присутствуют на Земле в своей официальной роли, остались здесь потому, что
                чувствуют душевную связь с человечеством. Именно ощущение ценности земной цивилизации дает им силы на
